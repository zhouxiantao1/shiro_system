package com.example.polly.enty;

public class Words {
	private int id;
	private String name;
	private String type;
	private String typeName;
	private String appearNum;
	private String appearFreq;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getTypeName() {
		return typeName;
	}
	public String getAppearNum() {
		return appearNum;
	}
	public void setAppearNum(String appearNum) {
		this.appearNum = appearNum;
	}
	public String getAppearFreq() {
		return appearFreq;
	}
	public void setAppearFreq(String appearFreq) {
		this.appearFreq = appearFreq;
	}
	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}
	
	
	
}
